            hash_index = self._get_hash(key)
            bucket = self.table[hash_index]

            # –ò—Ç–µ—Ä–∏—Ä—É–µ–º—Å—è –ø–æ —ç–ª–µ–º–µ–Ω—Ç–∞–º –±–∞–∫–µ—Ç–∞ —Å –∏—Ö –∏–Ω–¥–µ–∫—Å–∞–º–∏ –¥–ª—è –ø–æ–∏—Å–∫–∞ –∏ —É–¥–∞–ª–µ–Ω–∏—è –∫–ª—é—á–∞
